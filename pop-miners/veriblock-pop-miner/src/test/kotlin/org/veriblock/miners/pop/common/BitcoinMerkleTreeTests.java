    @Test
    public void NewTreeWhenOddNumberOfLeafNodes() {
        List<String> tx = new ArrayList<>();
        tx.add("0a34b7567c5c89f05ac33be7cb76f92669a93a22d295a4822bfe920db1f36b51");
        tx.add("830d12bc618e52c5d5688189b27d9fcea70c127544c1521eb2971d61456262ac");
        tx.add("df6e201cdcdf53e63fbfa4e80112d9247ef7327147cdda322be272dfe242cbcf");

        String merkleRoot = "1d98afa4a32e33ba6cf87af14bb0bee1a56acf4a58778abbc3cc1270998d87d3";

        BitcoinMerkleTree tree = new BitcoinMerkleTree(true, tx);

        Assert.assertTrue(merkleRoot.equalsIgnoreCase(tree.getMerkleRoot()));
        Assert.assertNotNull(tree.getPathFromTxID("0a34b7567c5c89f05ac33be7cb76f92669a93a22d295a4822bfe920db1f36b51"));
        Assert.assertNotNull(tree.getPathFromTxID("830d12bc618e52c5d5688189b27d9fcea70c127544c1521eb2971d61456262ac"));
        Assert.assertNotNull(tree.getPathFromTxID("df6e201cdcdf53e63fbfa4e80112d9247ef7327147cdda322be272dfe242cbcf"));
    }

